// Array Methods
let buah = ["Apel", "Pisang", "Mangga"];

// push(): Menambah elemen di akhir array
// Kegunaan: Untuk menambahkan satu atau lebih elemen ke akhir array
buah.push("Jeruk");
console.log(buah);

// pop(): Menghapus elemen terakhir dari array
// Kegunaan: Untuk menghapus dan mengembalikan elemen terakhir dari array
buah.pop();
console.log(buah);

// unshift(): Menambah elemen di awal array
// Kegunaan: Untuk menambahkan satu atau lebih elemen ke awal array
buah.unshift("Anggur");
console.log(buah);

// shift(): Menghapus elemen pertama dari array
// Kegunaan: Untuk menghapus dan mengembalikan elemen pertama dari array
buah.shift();
console.log(buah);

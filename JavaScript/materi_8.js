// While Loop
// Penjelasan: While loop adalah struktur pengulangan yang akan terus berjalan
// selama kondisi yang diberikan bernilai true.

// Kegunaan:
// 1. Melakukan iterasi dengan jumlah pengulangan yang tidak pasti
// 2. Mengeksekusi kode berulang kali berdasarkan suatu kondisi
// 3. Berguna untuk memproses data sampai kondisi tertentu terpenuhi

let i = 0;
while (i < 5) {
    console.log("Angka ke-" + i);
    i++;
} 

// Dalam contoh ini, loop akan berjalan 5 kali (dari 0 sampai 4)
// dan mencetak nomor urut setiap iterasi

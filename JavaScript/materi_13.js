// Template Literals
// Penjelasan: Template literals adalah fitur JavaScript yang memungkinkan
// pembuatan string multi-baris dan interpolasi ekspresi dalam string.

// Kegunaan:
// 1. Membuat string multi-baris tanpa karakter escape
// 2. Menyisipkan ekspresi JavaScript ke dalam string (interpolasi)
// 3. Membuat string yang lebih mudah dibaca dan dipelihara

let nama = "Siti";
let pesan = `Halo, nama saya ${nama}. Senang bertemu dengan Anda!`;

console.log(pesan);
// Output: Halo, nama saya Siti. Senang bertemu dengan Anda!

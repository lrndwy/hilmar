// Function dengan Return
// Penjelasan: Function 'tambah' menerima dua parameter (a dan b) dan mengembalikan hasil penjumlahan keduanya.
// Kegunaan:
// 1. Melakukan operasi penjumlahan dua angka
// 2. Mengembalikan hasil perhitungan untuk digunakan di bagian lain dari program
// 3. Memisahkan logika penjumlahan menjadi fungsi yang dapat digunakan kembali
function tambah(a, b) {
    return a + b;
}

// Memanggil function dan menyimpan hasilnya
let hasil = tambah(5, 3);

// Menampilkan hasil ke console
// Kegunaan: Memverifikasi bahwa function berjalan dengan benar
console.log(hasil);

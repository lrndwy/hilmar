let angka1 = 10;
let angka2 = 20;
let operator = "+";

if (operator == "+") {
    console.log(angka1 + angka2);
} else if (operator == "-") {
    console.log(angka1 - angka2);
} else if (operator == "*") {
    console.log(angka1 * angka2);
} else if (operator == "/") {
    console.log(angka1 / angka2);
}


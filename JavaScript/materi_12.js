// Arrow Function
// Penjelasan: Arrow function adalah cara singkat untuk menulis fungsi dalam JavaScript.
// Kegunaan:
// 1. Membuat kode lebih ringkas dan mudah dibaca
// 2. Mempertahankan konteks 'this' dari lingkup di sekitarnya
// 3. Ideal untuk fungsi sederhana dan callback

const kali = (a, b) => a * b;

// Contoh penggunaan arrow function
let hasilKali = kali(4, 5);
console.log(hasilKali); // Output: 20


